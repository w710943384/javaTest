package day_05;

public class DemoMath {
    public static void main(String[] args) {
        System.out.println(Math.abs(-5.5)); //绝对值
        System.out.println(Math.ceil(-2.1)); //向上取整
        System.out.println(Math.floor(3.9)); //向下取整,抹零
        System.out.println(Math.round(20.4)); //四舍五入

    }
}
